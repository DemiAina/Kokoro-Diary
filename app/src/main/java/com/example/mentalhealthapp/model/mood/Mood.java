package com.example.mentalhealthapp.model.mood;

/**
 * Represents the mood states for tracking a user's mood.
 * This enum defines three types of mood states: HAPPY, OK, and SAD.
 */
public enum Mood {
    /**
     * Represents a happy mood state.
     */
    HAPPY,

    /**
     * Represents an okay mood state, neither particularly happy nor sad.
     */
    OK,

    /**
     * Represents a sad mood state.
     */
    SAD,
}

